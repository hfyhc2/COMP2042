package World;

/**
 * This class defines the coordinate positions for objects 
 *
 */
public class coordinate {

	public int x;
	public int y;
	
	/**
	 * the constructor
	 * @param x coordinate
	 * @param Y coordinate
	 */
	public coordinate(int x,int y) {
		this.x=x;
		this.y=y;
	}
}
